// Different ways of defining a function in javascript

// 1. Function declaration
function add(a, b) {
  return a + b
}
console.log(add(1, 5))


























// 2. Function expression
const eq = function(a, b) {
  return a + b
}
console.log(eq(1, 5))





































// 3. Arrow functions
const add =  (a, b) => a + b

console.log(add(1, 5))